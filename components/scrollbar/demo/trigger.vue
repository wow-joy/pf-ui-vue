## zh-CN

`trigger="none"` 时滚动条一直显示，`trigger="hover"` 时滚动条在鼠标悬浮时显示。

## en-US

Setting `trigger="none"` will make scrollbar always shown. Setting `trigger="hover"` will make scrollbar shown on mouse hover.

