## zh-CN

按钮有6种类型：主按钮、次按钮、默认按钮、虚线按钮、文本按钮和链接按钮。主按钮在同一个操作区域最多出现一次。

## en-US

There are `primary` button, `secondary`, `default` button, `dashed` button, `text` button and `link` button in antd.

